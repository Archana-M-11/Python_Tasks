'''
1.Write a program that takes a sentence from the user converts it to
uppercase
lowercase
title case
swapcase
'''
n=input()
upper=n.upper()
lower=n.lower()
title=n.title()
swap=n.swapcase()
print('convert to upper case',upper)
print('convert to lower case',lower)
print('convert to title case',title)
print('convert to swap case',swap)